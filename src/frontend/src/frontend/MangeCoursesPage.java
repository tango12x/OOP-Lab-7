    /**
     * Event handler for when an instructor clicks on a course in the list.
     * It populates the lessons list for that course.
     */
    private void coursesListValueChanged(javax.swing.event.ListSelectionEvent evt) {
        if (!evt.getValueIsAdjusting()) {
            selectedCourse = coursesList.getSelectedValue();
            if (selectedCourse != null) {
                loadLessonsForCourse(selectedCourse);
                saveLessonButton.setEnabled(true); // Enable saving new lessons
            } else {
                lessonsListModel.clear();
                saveLessonButton.setEnabled(false); // Disable if no course is selected
            }
            clearLessonForm(); // Clear form when course selection changes
        }
    }

    /**
     * Event handler for when an instructor clicks on a lesson.
     * It loads the lesson's data into the editor form.
     */
    private void lessonsListValueChanged(javax.swing.event.ListSelectionEvent evt) {
        if (!evt.getValueIsAdjusting()) {
            selectedLesson = lessonsList.getSelectedValue();
            if (selectedLesson != null) {
                // Load lesson data into editor
                lessonTitleField.setText(selectedLesson.getTitle());
                lessonContentTextArea.setText(selectedLesson.getContent());

                // Update button states for editing
                saveLessonButton.setText("Save Edits");
                deleteLessonButton.setEnabled(true);
            }
        }
    }

    /**
     * Event handler for the "Save" button.
     * It either creates a new lesson or updates an existing one.
     */
    private void saveLessonButtonActionPerformed(java.awt.event.ActionEvent evt) {
        if (selectedCourse == null) {
            JOptionPane.showMessageDialog(this, "Please select a course first.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String title = lessonTitleField.getText().trim();
        String content = lessonContentTextArea.getText().trim();

        if (title.isEmpty() || content.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Title and Content cannot be empty.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (selectedLesson == null) {
            // This is a NEW lesson
            Lesson newLesson = service.addLessonToCourse(selectedCourse, title, content);
            if (newLesson != null) {
                JOptionPane.showMessageDialog(this, "Lesson '" + newLesson.getTitle() + "' added.");
            }
        } else {
            // We are EDITING an existing lesson
            service.updateLesson(selectedLesson, title, content);
            JOptionPane.showMessageDialog(this, "Lesson '" + selectedLesson.getTitle() + "' updated.");
        }

        // Refresh and clear
        loadLessonsForCourse(selectedCourse);
        clearLessonForm();
    }

    /**
     * Event handler for the "Delete" button.
     */
    private void deleteLessonButtonActionPerformed(java.awt.event.ActionEvent evt) {
        if (selectedLesson == null || selectedCourse == null) {
            return; // Should be impossible if button is enabled, but good practice
        }

        int choice = JOptionPane.showConfirmDialog(this,
                "Are you sure you want to delete the lesson '" + selectedLesson.getTitle() + "'?",
                "Confirm Deletion", JOptionPane.YES_NO_OPTION);

        if (choice == JOptionPane.YES_OPTION) {
            service.deleteLesson(selectedCourse, selectedLesson);
            JOptionPane.showMessageDialog(this, "Lesson deleted.");
            loadLessonsForCourse(selectedCourse);
            clearLessonForm();
        }
    }

    /**
     * Event handler for the "Clear Form" button.
     * Resets the editor to create a new lesson.
     */
    private void clearLessonFormButtonActionPerformed(java.awt.event.ActionEvent evt) {
        clearLessonForm();
    }



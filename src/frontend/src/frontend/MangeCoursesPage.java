    /**
     * Finds the courses created by this instructor and adds them to the list.
     */
    private void loadCreatedCourses() {
        coursesListModel.clear();
        ArrayList<String> courseIds = currentUser.getCreatedCourses();
        for (String courseId : courseIds) {
            Course course = service.findCourseById(courseId);
            if (course != null) {
                coursesListModel.addElement(course);
            }
        }
    }

    /**
     * Loads the lessons for a selected course into the lesson list.
     */
    private void loadLessonsForCourse(Course course) {
        lessonsListModel.clear();
        if (course != null) {
            for (Lesson lesson : course.getLessons()) {
                lessonsListModel.addElement(lesson);
            }
        }
    }

